.DEFINE
	NUM0 07H
	NUM1 43H
 
.ORG 0100H
	MVI A, NUM0
	STA 0002H	;STORES NUM0 IN DIR 0002H = 07H

	MVI A, NUM1
	STA 0003H	;STORES NUM1 IN DIR 0003H = 43H

	LHLD 0002H	;H<-0003H = 43H, L<-0002H = 07H
	XCHG 		;EXCHANGES CONTENTS OF H WITH D AND CONTENTS OF L WITH E
	
	MOV C, D	;C = 43H
	MVI D, 00H

	LXI H, 0000H	;H<-00H, L<-00H

TIMES:
	DAD D		;HL<-HL+DE (D = 00H, E = 07H)
	DCR C		;C<-C-1. . .
	JNZ TIMES	;C != 0

	SHLD 0004H	;H->0005, L->0004
	HLT